#include "main.h"

/**
*print_sign-check if a character is lower case
*@n: the character
*Return: 1 if the lower case is found
*/

int print_sign(int n)
{
{
	if (n > 0)
	return (1);
	_putchar ('+');
}
{
	if (n == 0)
	return (0);
	_putchar ('0');
}
{
	if (n < 0)
	return (-1);
	_putchar ('-');
}
	return (0);
}
